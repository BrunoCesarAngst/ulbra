a = float(input('Digite um número: '))
b = float(input('Digite um outro número: '))
som = a+b
sub = a-b
mul = a*b
div = a/b
print('A soma de {:.2f} e {:.2f} é {:.2f}.'.format(a, b, som))
print('A subtração de {:.2f} e {:.2f} é {:.2f}.'.format(a, b, sub))
print('A multiplicação de {:.2f} e {:.2f} é {:.2f}.'.format(a, b, mul))
print('A divisão de {:.2f} e {:.2f} é {:.2f}.'.format(a, b, div))
